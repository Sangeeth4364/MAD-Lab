package com.example.facebook;

import android.content.Intent;
import android.net.Uri;
import android.os.Bundle;
import android.view.View;
import android.widget.Button;

import androidx.appcompat.app.AppCompatActivity;
import androidx.core.graphics.Insets;
import androidx.core.view.ViewCompat;
import androidx.core.view.WindowInsetsCompat;

public class MainActivity extends AppCompatActivity {

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);

        // Handle Window Insets
        View mainView = findViewById(R.id.main);
        ViewCompat.setOnApplyWindowInsetsListener(mainView, (v, insets) -> {
            Insets systemBars = insets.getInsets(WindowInsetsCompat.Type.systemBars());
            v.setPadding(systemBars.left, systemBars.top, systemBars.right, systemBars.bottom);
            return insets;
        });

        // Set up the button
        Button facebookLoginButton = findViewById(R.id.button);
        facebookLoginButton.setOnClickListener(v -> {
            // Construct the Facebook login URL
            String facebookLoginUrl = "https://www.facebook.com";
            Intent browserIntent = new Intent(Intent.ACTION_VIEW, Uri.parse(facebookLoginUrl));
            startActivity(browserIntent);
        });
    }
}
